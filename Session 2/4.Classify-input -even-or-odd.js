function evenOrodd(num) {
    if (num % 2 === 0) {
        return "Even"
    }
    else {
        return "Odd"
    }
}

console.log(evenOrodd(5)); // Odd
console.log(evenOrodd(6)); // Even
console.log(evenOrodd(0)); // Even
console.log(evenOrodd(1)); // Odd
console.log(evenOrodd(2)); // Even
console.log(evenOrodd(3)); // Odd
